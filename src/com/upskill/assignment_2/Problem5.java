public class Problem5 {
	
	//This java program displays the multiplication table of 8 using a for loop

	public static void main(String[] args) {
		
		int j = 8;
		for(int i = 1; i <= 10; i++){
			System.out.println("Multiplication of " + j + "x" + i + " = " + j * i);
		}

	}

}
